@router.post("/seed-cuisine")
async def seed_cuisine(
    cuisine: str = Query("japanese", pattern="^(japanese|mediterranean|vegan|sports)$"),
    db: AsyncSession = Depends(get_db),
    user: User = Depends(get_current_user),
):
    dishes = CUISINES.get(cuisine)
    if not dishes:
        raise HTTPException(404, "Unknown cuisine")

    existing = {r[0] for r in (await db.execute(select(Recipe.name).where(Recipe.user_id == user.id))).all()}
    inserted = 0
    for name, total_g, ingredients in dishes:
        if name in existing:
            continue
        recipe = Recipe(id=uuid4(), user_id=user.id, name=name, total_weight_g=float(total_g), servings=1)
        db.add(recipe)
        await db.flush()
        for i, (ing_name, ing_g) in enumerate(ingredients):
            p = (await db.execute(select(Product).where(Product.name.ilike(f"%{ing_name}%")).limit(1))).scalar_one_or_none()
            db.add(RecipeIngredient(id=uuid4(), recipe_id=recipe.id,
                                    product_id=p.id if p else None,
                                    product_name=p.name if p else ing_name,
                                    amount_g=float(ing_g), sort_order=i))
        inserted += 1
    await db.commit()
    return {"cuisine": cuisine, "inserted": inserted, "skipped": len(dishes) - inserted}
