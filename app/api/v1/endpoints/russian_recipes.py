@router.post("/seed-russian")
async def seed_russian(
    db: AsyncSession = Depends(get_db),
    user: User = Depends(get_current_user),
):
    """Insert Russian dishes as recipes for current user. Idempotent — skips existing names."""
    existing = {
        r[0] for r in (await db.execute(
            select(Recipe.name).where(Recipe.user_id == user.id)
        )).all()
    }
    inserted = 0
    for name, total_g, ingredients in RUSSIAN_DISHES:
        if name in existing:
            continue
        recipe = Recipe(id=uuid4(), user_id=user.id, name=name, total_weight_g=float(total_g), servings=1)
        db.add(recipe)
        await db.flush()
        for sort_order, (ing_name, ing_grams) in enumerate(ingredients):
            # Try to match product
            p = (await db.execute(
                select(Product).where(Product.name.ilike(f"%{ing_name}%")).limit(1)
            )).scalar_one_or_none()
            ing = RecipeIngredient(
                id=uuid4(),
                recipe_id=recipe.id,
                product_id=p.id if p else None,
                product_name=p.name if p else ing_name,
                amount_g=float(ing_grams),
                sort_order=sort_order,
            )
            db.add(ing)
        inserted += 1
    await db.commit()
    return {"inserted": inserted, "skipped": len(RUSSIAN_DISHES) - inserted}
