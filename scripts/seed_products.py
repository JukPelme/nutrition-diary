async def seed(db_url: str | None = None):
    from sqlalchemy.ext.asyncio import create_async_engine, async_sessionmaker
    from sqlalchemy.dialects.postgresql import insert
    from app.models.product import Product

    url = db_url or "postgresql+asyncpg://nutrition:nutrition@localhost:5432/nutrition_diary"
    engine = create_async_engine(url, echo=False)
    session_factory = async_sessionmaker(engine, expire_on_commit=False)

    batch = []
    for item in PRODUCTS:
        name, cal, prot, fat, carbs, fiber, category, brand = item
        batch.append({
            "id": uuid4(),
            "name": name,
            "calories": cal,
            "protein": prot,
            "fat": fat,
            "carbohydrates": carbs,
            "fiber": fiber if fiber else None,
            "category": category,
            "brand": brand,
            "source": "seed",
            "source_id": name.lower().replace(" ", "_"),
            "serving_size": 100.0,
            "serving_unit": "g",
            "is_verified": True,
        })

    async with session_factory() as session:
        stmt = insert(Product).values(batch).on_conflict_do_nothing()
        result = await session.execute(stmt)
        await session.commit()
        print(f"Загружено {result.rowcount} продуктов из {len(PRODUCTS)}")

    await engine.dispose()


if __name__ == "__main__":
    asyncio.run(seed())
